export function generateComparisonData(speed) {
  const factor = speed / 240
  return [
    {
      metric: 'Resistencia aerodinámica',
      unit: 'mCd',
      kingfisher: parseFloat((40 * factor).toFixed(1)),
      eagle: parseFloat((120 * factor * 1.15).toFixed(1)),
      toucan: parseFloat((220 * factor * 1.3).toFixed(1)),
    },
    {
      metric: 'Eficiencia energética',
      unit: '%',
      kingfisher: parseFloat(Math.max(30, 95 - 5 * (factor - 1)).toFixed(1)),
      eagle: parseFloat(Math.max(20, 72 - 8 * (factor - 1)).toFixed(1)),
      toucan: parseFloat(Math.max(10, 45 - 10 * (factor - 1)).toFixed(1)),
    },
    {
      metric: 'Ruido estimado',
      unit: 'dB',
      kingfisher: parseFloat((15 * factor).toFixed(1)),
      eagle: parseFloat((38 * factor).toFixed(1)),
      toucan: parseFloat((72 * factor).toFixed(1)),
    },
    {
      metric: 'Estabilidad del flujo',
      unit: '%',
      kingfisher: parseFloat(Math.max(40, 98 - 8 * (factor - 1)).toFixed(1)),
      eagle: parseFloat(Math.max(20, 70 - 12 * (factor - 1)).toFixed(1)),
      toucan: parseFloat(Math.max(5, 35 - 15 * (factor - 1)).toFixed(1)),
    },
  ]
}

